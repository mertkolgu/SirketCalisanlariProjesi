    public ArrayList<Calisan> calisanlariListele() {
        ArrayList<Calisan> calisanList = new ArrayList<>();
        try {
            db.baglan();
            String sorgu = "SELECT * FROM calisanlar";
            statement = db.con.createStatement();
            resultSet = statement.executeQuery(sorgu);

            while (resultSet.next()) {
                int id = resultSet.getInt("id");
                String ad = resultSet.getString("ad");
                String soyad = resultSet.getString("soyad");
                String departman = resultSet.getString("departman");
                int maas = resultSet.getInt("maas");
                calisanList.add(new Calisan(id, ad, soyad, departman, maas));
            }
            return calisanList;
        } catch (SQLException e) {
            System.out.println("Hata \n" + e);
            return null;
        }
    }

    public void calisanlariGoruntule() {
        model.setRowCount(0);

        ArrayList<Calisan> calisanlar = new ArrayList<>();

        calisanlar = calisanlariListele();

        if (calisanlar != null) {
            for (Calisan calisan : calisanlar) {
                Object[] eklenecek = {calisan.getId(), calisan.getAd(), calisan.getSoyad(), calisan.getDepartman(), calisan.getMaas()};
                model.addRow(eklenecek);
            }
        }
    }

    public void calisanAra(String ara) {
        TableRowSorter<DefaultTableModel> tableRowSorter = new TableRowSorter<>(model);
        calisanTable.setRowSorter(tableRowSorter);
        tableRowSorter.setRowFilter(RowFilter.regexFilter(ara));
    }

