    private void calisanGuncelleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calisanGuncelleButtonActionPerformed
        mesajYazisiLabel.setText("");
        String ad = adTextField.getText();
        String soyad = soyadTextField.getText();
        String departman = departmanTextField.getText();
        int maas = Integer.valueOf(maasTextField.getText());
        int selectedRow = calisanTable.getSelectedRow();

        if (selectedRow == -1) {
            if (model.getRowCount() == 0) {
                mesajYazisiLabel.setText("Çalışanlar tablosu şu an boş.");
            } else {
                mesajYazisiLabel.setText("Lütfen güncellenecek bir çalışan seçin.");
            }
        } else {
            int id = (int) model.getValueAt(selectedRow, 0);
            calisanListele.calisanGuncelle(id, ad, soyad, departman, maas);
            calisanlariGoruntule();
            mesajYazisiLabel.setText("Çalışan başarıyla güncellendi.");
        }
    }//GEN-LAST:event_calisanGuncelleButtonActionPerformed

    private void calisanSilButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calisanSilButtonActionPerformed
        mesajYazisiLabel.setText("");
        int selectedRow = calisanTable.getSelectedRow();

        if (selectedRow == -1) {
            if (model.getRowCount() == 0) {
                mesajYazisiLabel.setText("Çalışanlar tablosu şu an boş.");
            } else {
                mesajYazisiLabel.setText("Lütfen silinecek bir çalışan seçin.");
            }
        } else {
            int id = (int) model.getValueAt(selectedRow, 0);
            calisanListele.calisanSil(id);
            calisanlariGoruntule();
            mesajYazisiLabel.setText("Çalışan başarıyla silindi.");
        }
    }//GEN-LAST:event_calisanSilButtonActionPerformed

